def _calc_wuge(surname: str, given_name: str) -> dict:
    """B61 修复: 五格完整计算

    五格 = 天格、人格、地格、外格、总格
    """
    if not surname or not given_name:
        return {}

    surname_strokes = sum(_lookup_strokes(c) for c in surname)
    given_strokes = [_lookup_strokes(c) for c in given_name]

    # 天格: 姓 + 1 (单姓) 或姓总笔画 (复姓)
    if len(surname) == 1:
        tian_ge = surname_strokes + 1
    else:
        tian_ge = surname_strokes

    # 人格: 姓末字 + 名首字
    ren_ge = _lookup_strokes(surname[-1]) + given_strokes[0] if given_strokes else 0

    # 地格: 名总笔画
    di_ge = sum(given_strokes)

    # 外格: 天格 + 地格 - 人格
    wai_ge = tian_ge + di_ge - ren_ge

    # 总格: 姓 + 名总笔画
    zong_ge = surname_strokes + di_ge

    return {
        '天格': tian_ge,
        '人格': ren_ge,
        '地格': di_ge,
        '外格': wai_ge,
        '总格': zong_ge,
    }

