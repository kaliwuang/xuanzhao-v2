        tian_jx = wuge["天格"].get("吉凶", "平")
        wai_jx = wuge["外格"].get("吉凶", "平")
        avg_tianwai = (jixiong_scores.get(tian_jx, 50) + jixiong_scores.get(wai_jx, 50)) / 2
        score += avg_tianwai * 0.10
        # 2026-06-13 修正：天格/外格大凶时额外扣分
