def extract_quote_with_blanks(lines, start, end):
    out = []
    for i in range(start - 1, end):
        if i < 0 or i >= len(lines):
            continue
        out.append(lines[i])
    while out and not out[0].strip():
        out.pop(0)
    while out and not out[-1].strip():
        out.pop()
    return "\n".join(out)


def extract_rules(quote_text):
    rules = []
    raw_sents = re.split(r"[。;；]", quote_text)
    keywords = (
        "宜", "忌", "不可", "须", "必", "用", "取", "先", "喜", "则吉", "则凶",
        "为大", "主大", "定主", "名曰", "名为", "一派", "为", "平人", "常人",
        "贫贱", "大贵", "大富", "小贵", "小富", "科甲", "状元", "进士", "贡",
        "夭", "孤", "寡", "残疾", "僧道", "武职", "文职", "可云", "可为",
        "可许", "可", "或", "或一", "必主", "必", "若", "如", "须", "须用",
        "皆", "皆可", "不吉", "不致", "如有", "如见", "若见", "得一", "得",
        "运入", "运行", "端", "端取", "执", "柱中", "一派", "不见", "见", "有",
    )
    seen = set()
    for s in raw_sents:
        s = s.strip()
        if not s:
            continue
        if len(s) < 4:
            continue
        is_rule = False
        for k in keywords:
            if k in s[:6] or k in s[:14]:
                is_rule = True
                break
        if not is_rule:
            continue
        if s in seen:
            continue
        seen.add(s)
        if len(s) > 200:
            s_short = s[:200] + "..."
        else:
            s_short = s
        rules.append("- **【判断】**: " + s_short)
    return rules


